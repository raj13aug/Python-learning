print(10 + 3)  # -> Addition
print(10 - 3)  # -> subtraction
print(10 * 3)  # -> multiplication

# Two different of division

print(10 / 3)  # -> division operator get a floating point number.
print(10 // 3)  # ->  If you want an integer, you use double slashes.

print(10 % 3)  # modulus which is remainder of a division,
print(10 ** 3)  # exponent is left to the power of right.

# Augmented assignment operator.
x = 10
x = x + 3  # you want to increment 10 by 3
x += 3    # assignment operator
