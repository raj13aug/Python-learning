#for loop to interate over each character in a string and then do something with it. Here's an example
for item in 'Python':
    print(item)

for item in ['Nataraj', 'Mano']:
    print(item)

for item in [1, 2, 3, 4]:
    print(item)
for item in range(10):
    print(item)

for item in range(5, 10):
    print(item)
# Two steps forward 
for item in range(5, 10, 2):
    print(item)

